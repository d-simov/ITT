package Labyrinth;

public class Indexes {
    int idx1;
    int idx2;

    public Indexes(int idx1, int idx2) {
        this.idx1 = idx1;
        this.idx2 = idx2;
    }

    public int getIdx1() {
        return idx1;
    }

    public void setIdx1(int idx1) {
        this.idx1 = idx1;
    }

    public int getIdx2() {
        return idx2;
    }

    public void setIdx2(int idx2) {
        this.idx2 = idx2;
    }
}
