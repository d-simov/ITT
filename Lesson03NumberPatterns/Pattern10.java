package Lesson03NumberPatterns;

public class Pattern10 {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.print((i + 1) + " ");
            }
            System.out.println();
        }
    }
}
