package Lesson04PracticeOnLoops;

public class  Task1 {

    public static void main(String[] args) {
        for (int i = -100; i <= 100; i++) {
            System.out.println(i);
        }
        System.out.println();

        for (int i = 100; i >= -100; i--) {
            System.out.println(i);
        }
    }
}
